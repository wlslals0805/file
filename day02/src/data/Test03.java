package data;

public class Test03 {
		public static void main(String[] args) {
			//변수(Variable)=데이터 기억장치

			int a=7000*2; //7000 *2를 계산해서 a라는 이름으로 저장(대입)
			int b=8000*3;//8000*3을 계산해서 b라는 이름으로 저장(대입)
			
			System.out.println(a); //a에 저장된 데이터를 출력 
			System.out.println(b);//b에 저장된 데이터를 출력
			System.out.println(a+b);
			//변수를 사용하면 유지보수가 용이하다.
 		
		}
		}


